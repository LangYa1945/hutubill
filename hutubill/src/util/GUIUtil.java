package util;
 
import java.awt.Color;
import java.awt.Dimension;
import java.io.File;
 
import javax.swing.ImageIcon;
import javax.swing.JButton;
import javax.swing.JComponent;
import javax.swing.JFrame;
import javax.swing.JOptionPane;
import javax.swing.JPanel;
import javax.swing.JTextField;
 
public class GUIUtil {
    private static String imageFolder = "e:/project/hutubill/img";
 
    public static void setImageIcon(JButton b, String fileName, String tip) {//给按钮设置图标和文本以及提示文字
        ImageIcon i = new ImageIcon(new File(imageFolder, fileName).getAbsolutePath());
        b.setIcon(i);
        b.setPreferredSize(new Dimension(61, 81));
        b.setToolTipText(tip);
        b.setVerticalTextPosition(JButton.BOTTOM);
        b.setHorizontalTextPosition(JButton.CENTER);
        b.setText(tip);
    }
 
    public static void setColor(Color color, JComponent... cs) {//多个组件设置前景色
        for (JComponent c : cs) {
            c.setForeground(color);
        }
    }
 
    /**
     * 
     * @param p
     * @param strechRate 拉伸比例1表示满屏幕
     */
    public static void showPanel(JPanel p,double strechRate) {
        GUIUtil.useLNF();
        JFrame f = new JFrame();
        f.setSize(500, 500);
        f.setLocationRelativeTo(null);
        CenterPanel cp = new CenterPanel(strechRate);
        f.setContentPane(cp);
        f.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        f.setVisible(true);
        cp.show(p);
    }
     
    public static void showPanel(JPanel p) {//默认比例
        showPanel(p,0.85);
    }   
 
    public static boolean checkNumber(JTextField tf, String input) {//校验一个组件内容是否是数字格式
        if (!checkEmpty(tf, input))//判空
            return false;
        String text = tf.getText().trim();
        try {
            Integer.parseInt(text);//字符串转为整形
            return true;
        } catch (NumberFormatException e1) {
            JOptionPane.showMessageDialog(null, input + " 需要是整数");
            tf.grabFocus();
            return false;
        }
    }
 
    public static boolean checkZero(JTextField tf, String input) {//校验一个组件的内容是否是零
        if (!checkNumber(tf, input))//判断数字
            return false;
        String text = tf.getText().trim();
 
        if (0 == Integer.parseInt(text)) {
            JOptionPane.showMessageDialog(null, input + " 不能为零");
            tf.grabFocus();
            return false;
        }
        return true;
    }
 
    public static boolean checkEmpty(JTextField tf, String input) {//校验一个输入框内容是否是空
        String text = tf.getText().trim();//除去空格
        if (0 == text.length()) {
            JOptionPane.showMessageDialog(null, input + " 不能为空");
            tf.grabFocus();
            return false;
        }
        return true;
 
    }
 
    public static int getInt(JTextField tf) { //转为整形
        return Integer.parseInt(tf.getText());
    }
 
    public static void useLNF() {//使用水晶皮肤。
        try {
            javax.swing.UIManager.setLookAndFeel("com.birosoft.liquid.LiquidLookAndFeel");
        } catch (Exception e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }
}